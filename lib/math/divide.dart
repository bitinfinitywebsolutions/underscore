/// Divide two numbers.
///
/// Arguments:
/// - `dividend` (num): The first number in a division.
/// - `divisor` (num): The second number in a division.
///
/// Returns:
/// - (num): Returns the quotient.
///
/// Example:
/// ```dart
/// print(divide(6, 4)); // Output: 1.5
/// ```
num divide(num dividend, num divisor) {
  return dividend / divisor;
}
