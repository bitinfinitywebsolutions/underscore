/// Adds two numbers.
///
/// Arguments:
/// - `augend` (number): The first number in an addition.
/// - `addend` (number): The second number in an addition.
///
/// Returns:
/// - (number): Returns the total.
///
/// Example:
/// ```dart
/// print(add(6, 4)); // Output: 10
/// ```
num add(num augend, num addend) {
  return augend + addend;
}
